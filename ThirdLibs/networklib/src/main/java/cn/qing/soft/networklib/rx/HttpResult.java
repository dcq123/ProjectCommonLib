package cn.qing.soft.networklib.rx;

/**
 * 通用的响应结果包装类
 */
public class HttpResult<T> {

    private boolean error;
    private T results;

    public T getResults() {
        return results;
    }

    public void setResults(T results) {
        this.results = results;
    }

    public boolean isError() {
        return error;
    }

    public void setError(boolean error) {
        this.error = error;
    }
}
